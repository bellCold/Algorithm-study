SELECT OUTS.ANIMAL_ID, OUTS.NAME
FROM ANIMAL_OUTS as OUTS
LEFT JOIN ANIMAL_INS as INS ON INS.ANIMAL_ID = OUTS.ANIMAL_ID
WHERE INS.ANIMAL_ID is null
ORDER BY OUTS.ANIMAL_ID;




















